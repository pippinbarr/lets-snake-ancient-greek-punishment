        for (let i = 1; i <= 3; i++) {
            let bit = this.snakeBodyGroup.create(x - i * this.GRID_SIZE, y, 'body');
            bit.setOrigin(0, 0);
            this.snake.unshift(bit)
        }

        this.snakeBitsToAdd = 0;
